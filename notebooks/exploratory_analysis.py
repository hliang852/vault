    pct_missing = 100 * (1 - present.mean())
    pct_estimate = (100 * df.loc[present, flag_col].fillna(False).astype(bool).mean()
                     if present.sum() else float('nan'))
    quality_rows.append({'field': value_col, 'pct_missing': round(pct_missing),
                          'pct_estimate_of_present': round(pct_estimate)})

