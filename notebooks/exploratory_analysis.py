def blank(v) -> bool:
    """Mirrors precedent_engine.py's blank(): True if unusable for a feature match."""
    if pd.isna(v):
        return True
    if isinstance(v, str) and v.strip() in ('', 'nan', 'Unclear/TBV', 'Other'):
        return True
    return False


