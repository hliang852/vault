def instigator_type(row) -> str:
    if row['category_group'] == 'Activist campaign' or row['has_activist_involvement']:
        return 'Activist-instigated'
    if row['acquirer_is_unlisted'] or row['category_group'] == 'Take-private / MBO':
        return 'Sponsor-instigated'
    return 'Strategic-instigated'


df['instigator_type'] = df.apply(instigator_type, axis=1)
