def conclusion_rate_by(df: pd.DataFrame, group_col: str, min_n_flag: int = 10) -> pd.DataFrame:
    """Descriptive conclusion-rate table: Concluded / (Concluded + Failed) per group.

    Other/Unclear rows are shown in their own column and excluded from the
    rate's denominator — never silently folded into "not a success" (see
    Section 1 for the corpus-wide Other/Unclear footnote). Groups with
    n < min_n_flag are explicitly flagged rather than presented as if
    reliable.
    """
    rows = []
    for group_val, sub in df.groupby(group_col, dropna=False):
        concluded = (sub['outcome_code'] == 'Concluded').sum()
        failed = (sub['outcome_code'] == 'Failed').sum()
        other = (sub['outcome_code'] == 'Other/Unclear').sum()
        denom = concluded + failed
        rate = concluded / denom if denom else float('nan')
        rows.append({
            group_col: group_val,
            'n': len(sub),
            'concluded': concluded,
            'failed': failed,
            'other_unclear': other,
            'conclusion_rate_excl_unclear': round(rate, 2) if denom else None,
            'flag_small_n': 'yes' if len(sub) < min_n_flag else '',
        })
    return pd.DataFrame(rows).sort_values('n', ascending=False).reset_index(drop=True)


