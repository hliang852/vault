def estimate_pairs(df: pd.DataFrame) -> list:
    pairs = []
    for col in df.columns:
        if col.endswith('_is_estimate'):
            base = col[: -len('_is_estimate')]
            value_col = base if base in df.columns else f'{base}_value'
            pairs.append((value_col if value_col in df.columns else None, col))
    return pairs


