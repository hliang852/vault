# %% [markdown]
# ## 3. Feature discriminativeness / rarity (feeds Part 2 weight review)
# For each feature `precedent_engine.py` currently scores on, how rare vs.
# common is it? This is the empirical basis for that engine's own stated
# logic ("rare, strong signal" vs "near-universal, weak signal") — lets us
# check whether hand-set weights track rarity. Weights are NOT changed
# here; that stays a Part 2 decision.

# %%
def boolean_rarity(df: pd.DataFrame, cols: list) -> pd.DataFrame:
    rows = []
    for col in cols:
        as_bool = df[col].fillna(False).astype(bool)
        rows.append({'feature': col, 'pct_true': round(100 * as_bool.mean()), 'n_true': int(as_bool.sum())})
    return pd.DataFrame(rows).sort_values('pct_true').reset_index(drop=True)


boolean_rarity(df, BOOLEAN_FEATURES + ACTIVIST_COLUMNS)

# %%
def categorical_concentration(df: pd.DataFrame, cols: list) -> pd.DataFrame:
    rows = []
    for col in cols:
        usable = df[col].apply(lambda v: not blank(v))
        vc = df.loc[usable, col].value_counts()
        top_share = vc.iloc[0] / vc.sum() if len(vc) else float('nan')
        rows.append({
            'feature': col, 'n_usable': int(usable.sum()), 'n_distinct_values': len(vc),
            'top_value': vc.index[0] if len(vc) else None,
            'top_value_share_pct': round(100 * top_share) if len(vc) else None,
        })
    return pd.DataFrame(rows).sort_values('top_value_share_pct').reset_index(drop=True)


categorical_concentration(df, CATEGORICAL_FEATURES)


# %% [markdown]
# ## 4. Feature co-occurrence / redundancy
# Jaccard similarity between boolean flags: if two features almost always
# co-occur, giving both independent weight in the similarity score
# double-counts one underlying signal. Pairs where either feature is true
# in fewer than 3 rows are excluded — co-occurrence on 1-2 rows is noise.

# %%
def jaccard(a: pd.Series, b: pd.Series) -> float:
    a, b = a.fillna(False).astype(bool), b.fillna(False).astype(bool)
    union = (a | b).sum()
    return (a & b).sum() / union if union else float('nan')


def top_cooccurring_pairs(df: pd.DataFrame, cols: list, min_support: int = 3, top_n: int = 20) -> pd.DataFrame:
    rows = []
    for c1, c2 in itertools.combinations(cols, 2):
        n1 = df[c1].fillna(False).astype(bool).sum()
        n2 = df[c2].fillna(False).astype(bool).sum()
        if n1 < min_support or n2 < min_support:
            continue
        j = jaccard(df[c1], df[c2])
        if j > 0:
            rows.append({'feature_a': c1, 'feature_b': c2, 'jaccard': round(j, 2), 'n_a': int(n1), 'n_b': int(n2)})
    return pd.DataFrame(rows).sort_values('jaccard', ascending=False).head(top_n).reset_index(drop=True)


top_cooccurring_pairs(df, BOOLEAN_FEATURES + ACTIVIST_COLUMNS)


# %% [markdown]
# ## 5. Pairwise comparability coverage
# `precedent_engine.py` only scores a feature match when BOTH rows in a
# pair are usable on it. Boolean flags (`has_*`/`structure_has_*`/
# `activist_*`) are always populated (False when absent) so their coverage
# is trivially 100% — the categorical features are where coverage loss
# actually happens, since `blank()` treats NaN/"Unclear/TBV"/"Other" as
# unusable.

# %%
def pairwise_categorical_coverage(df: pd.DataFrame, cols: list, pairs: list) -> pd.DataFrame:
    rows = []
    for col in cols:
        usable = df[col].apply(lambda v: not blank(v))
        both_usable = sum(1 for i, j in pairs if usable.iloc[i] and usable.iloc[j])
        rows.append({
            'feature': col, 'n_usable_rows': int(usable.sum()),
            'pct_pairs_both_usable': round(100 * both_usable / len(pairs)),
        })
    return pd.DataFrame(rows).sort_values('pct_pairs_both_usable').reset_index(drop=True)


